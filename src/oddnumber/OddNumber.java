package oddnumber;

public class OddNumber {

	public void OddNum(int n) {
		//logic for odd number
for(int i=0; i<=n; i++) {  //loop the values from 1 to n
	if(i%2!=0) {           // check condition for odd number
		System.out.println(i);  //print the output if condition is true
	}
}
	}

}
