package evennumber;

public class EvenNumber {
	
	
	public void EvenNum(int n) {
		//logic for even number
for(int i=1; i<=n; i++) {  //loop the values from 1 to n
	if(i%2==0) {           // check condition for even number
		System.out.println(i);  //print the output if condition is true
	}
}
	}

}
